
// for (valor inicial; condicion; pasos)
for (var i = 0; i<10; i++) {
	console.log( i );
}

// i++ es igual que i = i + 1
for (var n = 0; n <= 10; n = n + 1) {
	console.log( n );
}

// muestra impares
for (var n = 1; n <= 10; n = n + 2) {
	console.log( n );
}

// muestra pares
for (var n = 0; n <= 10; n = n + 2) {
	console.log( n );
}
// o
for (var n = 1; n <= 10; n = n + 2) {
	console.log( n + 1);
}